## Function 'makeCacheMatrix' creates a cached matrix object
## Function 'cacheSolve' inverts a CacheMatrix object

## Function to create a cached matrix object from a regular matrix

makeCacheMatrix <- function(x = matrix()) {
    i <- NULL
    set <- function(y) {
        x <<- y
        i <<- NULL
    }
    get <- function() x
    setinv <- function(inv) i <<- inv
    getinv <- function() i
    list(set = set, get = get,
         setinv = setinv,
         getinv = getinv)
}


## Function to perform an equivalent of solve on cached matrix object;
## will returned stored inverse result on repeated calls

cacheSolve <- function(x, ...) {
    i <- x$getinv()
    if(!is.null(i)) {
        message("getting cached data")
        return(i)
    }
    data <- x$get()
    i <- solve(data, ...)
    x$setinv(i)
    i
}